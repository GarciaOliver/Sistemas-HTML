    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtCodPrincipal.getText().isEmpty() && !txtCodAuxiliar.getText().isEmpty() && !txtCantidad.getText().isEmpty()
                && !txtDescripcion.getText().isEmpty() && !txtSubsidio.getText().isEmpty() && !txtDescuento.getText().isEmpty()
                && !txtPrecio.getText().isEmpty()) {
            
            productos=new ArrayList<>();
            String[] nuevoProduct = {txtCodPrincipal.getText(), txtCodAuxiliar.getText(), txtCantidad.getText(), txtDescripcion.getText(),txtPrecio.getText(),
                txtSubsidio.getText(), txtDescuento.getText()};
            productos.add(nuevoProduct);
            
            txtCodPrincipal.setText("");
            txtCodAuxiliar.setText("");
            txtCantidad.setText("");
            txtDescripcion.setText("");
            txtPrecio.setText("");
            txtSubsidio.setText("");
            txtDescuento.setText("");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Complete todos los campos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

